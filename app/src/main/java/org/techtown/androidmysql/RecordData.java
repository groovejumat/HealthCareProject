package org.techtown.androidmysql;

public class RecordData {
    private String email;
    private String exersize_time;
    private String kcal;
    private String change_weight;
    private String program_num;
    private String exersize_date;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getExersize_time() {
        return exersize_time;
    }

    public void setExersize_time(String exersize_time) {
        this.exersize_time = exersize_time;
    }

    public String getKcal() {
        return kcal;
    }

    public void setKcal(String kcal) {
        this.kcal = kcal;
    }

    public String getChange_weight() {
        return change_weight;
    }

    public void setChange_weight(String change_weight) {
        this.change_weight = change_weight;
    }

    public String getProgram_num() {
        return program_num;
    }

    public void setProgram_num(String program_num) {
        this.program_num = program_num;
    }

    public String getExersize_date() {
        return exersize_date;
    }

    public void setExersize_date(String exersize_date) {
        this.exersize_date = exersize_date;
    }
}
